<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;

class Vendor extends Model
{
    //
    protected $fillable = [
       'user_id',
       'store_name',
        'phone',
        'address',
        'image',
    ];

        public function user(){
        return $this->belongsTo(User::class);
    }

        public function products()
    {
        return $this->hasMany(VendorProduct::class);
    }

}
